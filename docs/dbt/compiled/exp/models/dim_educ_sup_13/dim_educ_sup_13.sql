

SELECT
    CAST("NU_ANO_CENSO" AS INT) AS "ANO",
    CAST("CO_MUNICIPIO_IES" AS INT) AS "FK_MUNICIPIO_CODIGO",
    SUM("QT_DOC_EX_MEST") AS "QT_DOC_EX_MEST",
    SUM("QT_DOC_EX_DOUT") AS "QT_DOC_EX_DOUT",
    SUM("QT_DOC_EXE") AS "QT_DOC_EXE"
